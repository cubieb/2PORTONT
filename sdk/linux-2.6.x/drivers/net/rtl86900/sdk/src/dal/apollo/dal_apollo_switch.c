int32
dal_apollo_switch_phyPatch(dal_apollo_phy_patch_type_t type)
{
    int i;
    int ret;
    uint16 phy;
    dal_apollo_phy_data_t *patchArray;
    uint32   patchSize,wData, cnt = 0;
    uint16   rData;
    uint32   chipId,revID;

    if(type > APOLLO_PATCH_END)
        return RT_ERR_INPUT;

    /*phy patch done*/
    wData = 0;
    if ((ret = reg_field_write(WRAP_GPHY_MISCr,PATCH_PHY_DONEf,&wData)) != RT_ERR_OK)
    {
        RT_ERR(ret, (MOD_SWITCH|MOD_DAL), "");
        return ret;
    }


    /* delay 10ms */
    osal_time_mdelay(10);

    drv_swcore_cid_get(&chipId,&revID);

    if(revID == CHIP_REV_ID_0)
    {
        if(APOLLO_PATCH_PHY == type)
        {
            patchArray = phyPatchArray;
            patchSize = sizeof(phyPatchArray)/sizeof(dal_apollo_phy_data_t);
        }
        else
        {
            patchArray = eeePatchArray;
            patchSize = sizeof(eeePatchArray)/sizeof(dal_apollo_phy_data_t);
        }
    }
    else
    {
        if(APOLLO_PATCH_PHY == type)
        {
            patchArray = phyPatchArrayRevB;
            patchSize = sizeof(phyPatchArrayRevB)/sizeof(dal_apollo_phy_data_t);
        }
        else
        {
            patchArray = NULL;
            patchSize = 0;
        }

    }
    /*set phy reset*/
    wData = 1;
    if ((ret = reg_field_write(SOFTWARE_RSTr,CMD_GPHY_RST_PSf,&wData)) != RT_ERR_OK)
    {
        RT_ERR(ret, (MOD_SWITCH|MOD_DAL), "");
        return ret;
    }

    do
    {
        if((ret=apollo_interPhy_read(4, 0xA46, 21, &rData))!=RT_ERR_OK)
        {
            RT_ERR(ret, (MOD_SWITCH|MOD_DAL), "");
            return ret;
        }
        cnt++;
        if(cnt > 10000)
        {
            break;
        }
    }
    while( ((rData & 0x0700) >> 8) != 0x2 );

    if(cnt > 10000)
    {
        /* Can't polling PHY ready */
        RT_ERR(ret, (MOD_SWITCH|MOD_DAL), "");
        return ret;
    }

    /*start patch phy*/
    for(i=0 ; i<patchSize ; i++)
    {
        if(patchArray[i].phy == DAL_APOLLO_BROADCAST_PHYID)
        {
            for(phy = 0; phy < 5 ; phy++)
            {
                if((ret=apollo_interPhy_write(phy,
                                      patchArray[i].page,
                                      patchArray[i].reg,
                                      patchArray[i].data))!=RT_ERR_OK)
                {
                    RT_ERR(ret, (MOD_SWITCH|MOD_DAL), "");
                    return ret;

                }
            }
        }
        else
        {
            if((ret=apollo_interPhy_write(patchArray[i].phy,
                                  patchArray[i].page,
                                  patchArray[i].reg,
                                  patchArray[i].data))!=RT_ERR_OK)
            {
                RT_ERR(ret, (MOD_SWITCH|MOD_DAL), "");
                return ret;

            }
        }
    }

    return RT_ERR_OK;
}



